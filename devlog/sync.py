#!/usr/bin/env python3
"""
DevLog Sync Script

Features:
1. Read config from ~/.devlog/config.md (or DEVLOG_ROOT env var)
2. Scan daily/ logs, extract @ProjectName tags, generate projects/
3. Archive inbox.md to daily/ (auto-distribute by timestamp)
4. Generate daily statistics table
5. Generate weekly report

Usage:
    python -m devlog.sync                    # Default: sync today + generate stats
    python -m devlog.sync --sync             # Sync daily to projects
    python -m devlog.sync --archive-inbox    # Archive inbox.md
    python -m devlog.sync --daily-stats      # Generate statistics
    python -m devlog.sync --weekly           # Generate weekly report
"""

import argparse
import logging
import os
import re
import sys
from datetime import datetime, timedelta
from pathlib import Path

logging.basicConfig(
    level=logging.INFO,
    format="[%(levelname)s] %(message)s",
)

from .config import DevLogConfig


class DevLogSync:
    def __init__(self, root_dir=None):
        self.config = DevLogConfig(root_dir)
        self.root = self.config.root_dir
        self.symbols = self.config.get_symbols()

    def _daily_path(self, date_str):
        return self.root / "daily" / f"{date_str}.md"

    def _ensure_daily(self, date_str):
        """Ensure daily file exists, create from template if missing."""
        path = self._daily_path(date_str)
        if path.exists():
            return path

        lang = self.config.language
        if lang == "auto":
            lang = self.config._detect_language()

        template_name = "daily.md" if lang == "zh" else "daily.en.md"
        template = self.root / ".devlog" / "templates" / template_name

        weekday_map = {
            "Mon": "周一", "Tue": "周二", "Wed": "周三",
            "Thu": "周四", "Fri": "周五", "Sat": "周六", "Sun": "周日"
        }
        weekday_en = datetime.strptime(date_str, "%Y-%m-%d").strftime("%a")
        weekday_label = weekday_map.get(weekday_en, weekday_en) if lang == "zh" else weekday_en

        if template.exists():
            content = template.read_text(encoding="utf-8")
        else:
            content = self._default_daily_template(lang=lang)

        content = content.replace("{{DATE}}", date_str).replace("{{WEEKDAY}}", weekday_label)
        path.parent.mkdir(parents=True, exist_ok=True)
        path.write_text(content, encoding="utf-8")
        return path

