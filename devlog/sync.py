            f"## 🧠 Reflections\n\n"
            f"<!-- Optional: filled during daily review -->\n\n"
            f"## 📊 Daily Stats\n\n"
            f"> Auto-generated by `sync.py --daily-stats`\n\n"
            f"<!-- STATS_TABLE_START -->\n"
            f"| Type | Count | Percentage |\n"
            f"|------|-------|------------|\n"
            f"| Note {s['note']} | 0 | 0% |\n"
            f"| Problem {s['problem']} | 0 | 0% |\n"
            f"| Learning {s['learning']} | 0 | 0% |\n"
            f"| Idea {s['idea']} | 0 | 0% |\n"
            f"| Todo {s['todo']}→{s['done']} | 0→0 | 0% |\n"
            f"<!-- STATS_TABLE_END -->\n\n"
            f"## 😊 Mood Tracker\n\n"
